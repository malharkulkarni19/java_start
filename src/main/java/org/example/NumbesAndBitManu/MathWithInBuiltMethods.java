package org.example.NumbesAndBitManu;

public class MathWithInBuiltMethods {
    public static void main(String[] args) {
        System.out.println(Math.abs(-11));  // turns negative value into positive value
        System.out.println(Math.max(11,12));  // finds max number in these 2 numbers
        System.out.println(Math.min(11,12));  // finds min number in these 2 numbers

        System.out.println((int)Math.pow(2,2));  // calculates 2 to power 2, that's is 4
        System.out.println((int)Math.sqrt(64));  // calculates square root
    }
}
