package org.example.NumbesAndBitManu;

public class BItwiseOperations {
    public static void main(String[] args) {
        System.out.println(5 & 7);       //bitwise and operation
        System.out.println(5 | 7);       //bitwise or operation
        System.out.println(5 ^ 7);       //bitwise xor operation
        System.out.println(~5);          //bitwise complement
    }
}
